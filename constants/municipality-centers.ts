export function getMunicipalityCenter(
  prefecture: string,
  municipality: string
): { lat: number; lng: number } | null {
  const p = MUNICIPALITY_CENTERS[prefecture.trim()]
  if (!p) return null
  return p[municipality.trim()] ?? null
}

export function getPrefectures(): string[] {
  return Object.keys(MUNICIPALITY_CENTERS)
}

export function getMunicipalities(prefecture: string): string[] {
  return Object.keys(MUNICIPALITY_CENTERS[prefecture.trim()] ?? {})
}

