export const MUNICIPALITY_CENTERS: Record<string, Record<string, LatLng>> = (() => {
  const out: Record<string, Record<string, LatLng>> = {}
  for (const [pref, muni, lat, lng] of _CENTERS) {
    if (!out[pref]) out[pref] = {}
    out[pref][muni] = { lat, lng }
  }
  return out
})()

export function listPrefectures(): string[] {
  return Object.keys(MUNICIPALITY_CENTERS)
}

export function listMunicipalities(pref: string): string[] {
  return Object.keys(MUNICIPALITY_CENTERS[pref] ?? {})
}

export function getMunicipalityCenter(pref: string, muni: string): LatLng | null {
  const p = MUNICIPALITY_CENTERS[pref.trim()]
  if (!p) return null
  return p[muni.trim()] ?? null
}
